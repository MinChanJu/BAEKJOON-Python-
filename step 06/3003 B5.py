k,q,r,b,n,p=map(int,input().split())
if k!=1:
    print(1-k,end=' ')
else:
    print(0,end=' ')
if q!=1:
    print(1-q,end=' ')
else:
    print(0,end=' ')
if r!=2:
    print(2-r,end=' ')
else:
    print(0,end=' ')
if b!=2:
    print(2-b,end=' ')
else:
    print(0,end=' ')
if n!=2:
    print(2-n,end=' ')
else:
    print(0,end=' ')
if p!=8:
    print(8-p,end=' ')
else:
    print(0)